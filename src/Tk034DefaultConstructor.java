

public class Tk034DefaultConstructor {
	int value;  //default integer value is 0.
	boolean b; //default boolean value false
	public static void main(String[] args) {
		Tk034DefaultConstructor obj = new Tk034DefaultConstructor(); //this is default constructor
		System.out.println(obj.value);
		System.out.println(obj.b);

	}

}
