
public class Tk073TwoDimensionalArrayExample {

	public static void main(String[] args) {
		int[][] myarray = {
							{2,4,5},
							{1,7,9,3},
							{8}
						  };
		
		System.out.println(myarray[0].length); //gives how many elements present in 0th index array
		System.out.println(myarray[1].length);
		System.out.println(myarray[2].length);
	}

}
